package com.breeze.framework.entity;

/**
 * @author JuanChensh
 *
 */
public class InternetUse {
	private String id;
	private String loanId;
	private String interfaceId;
	private String contractNo;
	private String fileType;
	private String onlinePath;
	private String downloadPath;
	
	/**
	 * @return the id
	 */
	public String getId() {
		return id;
	}
	/**
	 * @param id the id to set
	 */
	public void setId(String id) {
		this.id = id;
	}
	/**
	 * @return the loanId
	 */
	public String getLoanId() {
		return loanId;
	}
	/**
	 * @param loanId the loanId to set
	 */
	public void setLoanId(String loanId) {
		this.loanId = loanId;
	}
	/**
	 * @return the interfaceId
	 */
	public String getInterfaceId() {
		return interfaceId;
	}
	/**
	 * @param interfaceId the interfaceId to set
	 */
	public void setInterfaceId(String interfaceId) {
		this.interfaceId = interfaceId;
	}
	/**
	 * @return the contractNo
	 */
	public String getContractNo() {
		return contractNo;
	}
	/**
	 * @param contractNo the contractNo to set
	 */
	public void setContractNo(String contractNo) {
		this.contractNo = contractNo;
	}
	/**
	 * @return the fileType
	 */
	public String getFileType() {
		return fileType;
	}
	/**
	 * @param fileType the fileType to set
	 */
	public void setFileType(String fileType) {
		this.fileType = fileType;
	}
	/**
	 * @return the onlinePath
	 */
	public String getOnlinePath() {
		return onlinePath;
	}
	/**
	 * @param onlinePath the onlinePath to set
	 */
	public void setOnlinePath(String onlinePath) {
		this.onlinePath = onlinePath;
	}
	/**
	 * @return the downloadPath
	 */
	public String getDownloadPath() {
		return downloadPath;
	}
	/**
	 * @param downloadPath the downloadPath to set
	 */
	public void setDownloadPath(String downloadPath) {
		this.downloadPath = downloadPath;
	}
	
	

}
